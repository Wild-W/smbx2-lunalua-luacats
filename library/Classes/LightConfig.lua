---@meta


--- Configuration for light effects used by Blocks, NPCs, and BGOs.
---@class LightConfig
--- @field lightoffsetx number Horizontal offset of the light source relative to the center of the object. Default: 0
--- @field lightoffsety number Vertical offset of the light source relative to the center of the object. Default: 0
--- @field lightradius number Radius of the light source. Default: 0
--- @field lightbrightness number Brightness of the light source. Default: 1
--- @field lightflicker boolean Whether or not the light should slightly flicker. Default: `false`
--- @field lightcolor Color The color of the light source. Can either be the name of a color constant, or a hex value (i.e. 0xFFFFFFFF). Default: white
